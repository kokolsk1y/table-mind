export function buildSystemPrompt(style, catalog) {
	const styleConfig = STYLES[style] || STYLES.detailed;
	return `${styleConfig.intro}

${MENU_RULES}

КАТАЛОГ РЕСТОРАНА «ЯНТАРНЫЙ БЕРЕГ»:
${catalog}`;
}

