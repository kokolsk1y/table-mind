/**
 * Системные промпты для AI-агентов TableMind.
 *
 * Multi-tenant: имя ресторана подставляется через {{restaurant}} плейсхолдер.
 * Multi-lang: имя языка подставляется через {{lang}} (полное имя, "русский"/"английский"/...).
 *   AI обязан отвечать строго на этом языке вне зависимости от языка вопроса.
 */

const MENU_RULES = `
ЖЁСТКИЕ ПРАВИЛА:
1. Ты можешь рекомендовать ТОЛЬКО блюда из каталога ниже. Никаких других.
2. Цены, аллергены, состав — только из каталога. Если информации нет — скажи «уточните у официанта» (на языке гостя).
3. Если гость спрашивает о блюде, которого нет в каталоге — скажи честно: «Этого блюда у нас нет, но могу предложить похожее».
4. Никогда не придумывай ингредиенты, которых нет в описании.
5. При вопросах об аллергенах: если поле allergens пустое — скажи «точные данные об аллергенах уточните у официанта».
6. НЕ показывай технические ID блюд. Используй только красивые названия.
7. ОТВЕЧАЙ СТРОГО НА ЯЗЫКЕ: {{lang}}. Не переключайся на другой язык даже если гость спросил на нём.
   Имена блюд бери из каталога как есть (они уже на нужном языке).
`;

/** @type {Record<string, {label: string, intro: string}>} */
export const STYLES = {
	detailed: {
		label: "Расскажи подробно",
		intro: `Ты — дружелюбный AI-помощник ресторана «{{restaurant}}».
Рассказывай о блюдах кратко но увлечённо: с чем сочетается, что особенного.
Отвечай в 2-3 предложения. Не больше.`
	},
	brief: {
		label: "Кратко и по делу",
		intro: `Ты — AI-помощник ресторана «{{restaurant}}».
Отвечай максимально кратко: название, цена, аллергены. 1-2 предложения.`
	},
	guide: {
		label: "Помоги выбрать",
		intro: `Ты — AI-помощник ресторана «{{restaurant}}».
Помоги гостю выбрать. Задай один короткий уточняющий вопрос, затем предложи 2-3 варианта.
Будь кратким — максимум 3-4 предложения.`
	}
};

/** @type {Record<string, string>} */
const LANG_NAMES = {
	ru: "русский",
	en: "английский",
	de: "немецкий",
	pl: "польский",
	lt: "литовский",
	fr: "французский",
	it: "итальянский",
	es: "испанский",
	fi: "финский",
	sv: "шведский",
	no: "норвежский",
	zh: "китайский (упрощённый)"
};

/**
 * @typedef {object} BuildPromptOptions
 * @property {string=} restaurantName — имя ресторана. Дефолт "наш ресторан".
 * @property {string=} lang — код языка ответа. Дефолт "ru".
 */

/**
 * @param {string} style — ключ из STYLES
 * @param {string} catalog — отформатированный каталог (formatCatalogForAI)
 * @param {BuildPromptOptions} [opts]
 */
export function buildSystemPrompt(style, catalog, opts = {}) {
	const styleConfig = STYLES[style] || STYLES.detailed;
	const restaurantName = opts.restaurantName || "наш ресторан";
	const langCode = opts.lang || "ru";
	const langName = LANG_NAMES[langCode] || LANG_NAMES.ru;

	const intro = styleConfig.intro.replace(/{{restaurant}}/g, restaurantName);
	const rules = MENU_RULES.replace(/{{lang}}/g, langName);

	return `${intro}

${rules}

КАТАЛОГ РЕСТОРАНА «${restaurantName}»:
${catalog}`;
}

export const VERIFIER_PROMPT = `Ты — верификатор ответов AI-помощника ресторана.
Тебе дан ответ помощника и каталог блюд. Проверь:
1. Все упомянутые блюда есть в каталоге?
2. Цены совпадают?
3. Аллергены не выдуманы?
4. Ингредиенты соответствуют описанию?

Ответь СТРОГО в формате JSON:
{"verdict": "ok"} — если всё корректно
{"verdict": "warning", "note": "краткое описание проблемы"} — если найдено несоответствие

Ничего кроме JSON не пиши.`;
